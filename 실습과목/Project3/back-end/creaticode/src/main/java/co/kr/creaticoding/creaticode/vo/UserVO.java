package co.kr.creaticoding.creaticode.vo;

/**
 * @author creco
 *
 */
public class UserVO {
	private String user_id;
	private String oauth_uid;
	private String oauth_name;
	private String password;
	private String profile;
	
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getOauth_uid() {
		return oauth_uid;
	}
	public void setOauth_uid(String oauth_uid) {
		this.oauth_uid = oauth_uid;
	}
	public String getOauth_name() {
		return oauth_name;
	}
	public void setOauth_name(String oauth_name) {
		this.oauth_name = oauth_name;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getProfile() {
		return profile;
	}
	public void setProfile(String profile) {
		this.profile = profile;
	}
	
	    
}
